% subsequent records of the group bear corrected sounding values at 100 metre
% intervals,   starting  at  200  metres  (in  the  sequence   described  for
% CORRECTN.DAT above).
% 
% 
% Data record formats (for all records):-
% 
% Columns    Format              Contents
%  
%  1 - 2       I2       File identifier (always set to ' 4').
%  3 - 8       I6       Record number within  file  (starting at '     1' for
%                       first record in file). This also indicates the record
%                       number  of  the  direct access file on which the data
%                       value should be stored.
%  9 - 14      I6       Data value. This may be an index pointer,  correction
%                       area number or corrected sounding value (see descrip-
%                       tion of file format for details).
% 
% 
% A  subroutine which will scan these data (when they are stored in a  direct
% access  file)  and  return the corrected sounding value  from   a  supplied
