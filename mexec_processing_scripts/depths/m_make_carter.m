% 
% 
% Introduction
% ============
% 
% In 1980 a Third Edition of Echo-Sounding Correction Tables was published by
% the U.K.  Hydrographic department to  replace Matthews Tables.  The  tables
% were extensively revised to incorporate the large number of temperature and
% salinity  measurements obtained since 1939 and use an improved formula  for
% the speed of sound in sea water derived in recent years.   Computations for
% the  revised tables were carried out by D.J.T.  Carter of the Institute  of
% Oceanographic Sciences,  Wormley,  Surrey  using oceanographic station data
% provided   by  the  United  States  National  Oceanographic  Data   Centre,
% Washington.  The revised tables,   together with a detailed description  of
% their preparation, are contained in: 
% 
% 
%               ++++++++++++++++++++++++++++++++++++++++++++++
%               +                                            +
%               +   Echo-Sounding Correction Tables,         +
%               +   3rd Edition.   N.P. 139                  +
%               +   Hydrographic Department,                 +
%               +   Ministry of Defence, 1980.               +
%               +                                            +
%               +   Available from Admiralty Chart Agents.   +
%               +                                            +
%               ++++++++++++++++++++++++++++++++++++++++++++++
% 
% 
% In 1982 the Twelfth International Hydrographic Conference at Monaco decided
% to adopt the Third Edition Tables in place of Matthews  Tables.
% 
% The  Third  Edition Tables are applicable for use throughout the  world  in
% water  depths greater than 200 metres,  and cover  depth to the sea bed  in
% each  of  85 echo-sounding correction areas.  The tables are  expressed  in
% metres only, and assume an  echo-sounder velocity of 1500 metres/second. As
% the  boundaries  between  echo-sounding correction areas  lie  along  exact
% degrees of  latitude and longitude,  the tables are particularly suited for
% automatic  use on computerised systems. Although the published tables   are
% listed  at  10 metre intervals,  values between 100  metre  intervals  were
% derived by linear interpolation,  so only 100 metre values  need be  stored
% for access by a computer program.
% 
% A  computerised version of the tables (third edition),  originally  written
% for  the  IBM 1800 computer on RRS Discovery,  and also implemented on  the
% NERC  shipborne PDP/11 computers,  enables echo-soundings to be   corrected
% automatically given the ship position. This disk contains copies of the two
% FORTRAN subroutines necessary to  produce these corrections,  together with
% the  requisite data,  i.e.  the computerised echo-sounding correction  area
% definitions and correction tables. 
% 
% 
%                 *****************************************
%                 *                                       *
%                 *                                       *
%                 *         DIRECTORY DESCRIPTION         *
%                 *                                       *
%                 *                                       *
%                 *****************************************
% 
% 
% The directory contains seven files.  All files are standard  IBM  PC  ASCII  
% text  files  with variable length records,  each record being terminated by  
% the carriage-return/linefeed characters.
% 
% 
% The file contents are as follows:
% 
%   README.TXT    -  (this  text) contains documentation of the  contents  of
%                    the directory.
% 
%   BOUNDARY.DAT  -  contains   echo-sounding   correction   area    boundary
%                    definitions expressed in latitudinal bands.
% 
%   CORRECTN.DAT  -  contains   echo-sounding    correction   tables    (1500
%                    metres/second) for each correction area.
% 
%   BOUNDARY.IDX  -  contains indexed echo-sounding correction area  boundary 
%                    definitions in an expanded form suitable for loading  to
%                    a direct access file for use in automatic application of
%                    correction factors  by computer.  
% 
%   CORRECTN.IDX  -  contains  indexed echo-sounding correction tables in  an
%                    expanded  form suitable for loading to a  direct  access 
%                    file.
% 
%   FINAR.FTN     -  contains  a listing of a FORTRAN subroutine  (subroutine
%                    FINAR) which will return the appropriate correction area
%                    from   supplied  values of latitude and  longitude  when
%                    used in conjunction with a direct access file  generated
%                    from the data in BOUNDARY.IDX.
% 
%   DCORR.FTN     -  contains  a listing of a FORTRAN subroutine  (subroutine
%                    DCORR)  which  will return the corrected  sounding  from
%                    values  of   observed depth and correction  area  number
%                    when  used  in  conjunction with a  direct  access  file
%                    generated from the data in CORRECTN.IDX. 
% 
% 
%   ***********************************************************************
%   *                                                                     *
%   *                                                                     *
%   *                    FILE FORMAT DESCRIPTIONS                         *
%   *                                                                     *
%   *                                                                     *
%   *=====================================================================*
%   *                                                                     *
%   *    README.TXT      -   plain language text records                  *
%   *                                                                     *
%   *    FINAR.FTN       -   FORTRAN statement records                    *
%   *    DCORR.FTN                                                        *
%   *                                                                     *
%   *    BOUNDARY.DAT    -   data records as described below              *
%   *    CORRECTN.DAT                                                     *
%   *    BOUNDARY.IDX                                                     *
%   *    CORRECTN.IDX                                                     *
%   *                                                                     *
%   ***********************************************************************
% 
% 
% BOUNDARY.DAT :  Echo-sounding   correction   area   boundary    definitions
%                 expressed in latitudinal bands
% 
% 
% File contains 579 logical records.  
% 
% The  area boundary information for each degree of latitude is contained  in
% a group of two or more records.  The first record identifies  the  latitude
% band  and indicates the number of longitude band/area number data pairs  to
% be  found on the following record(s).  The  second and  subsequent  records
% hold longitude band and area number information.   
% 
% 
% Data record formats (for each group of records):-
% 
% Record   Columns    Format              Contents
% 
%    0      1 - 2       I2      File identifier (always set to ' 1').
%    0      3 - 6       I4      Latitude in degrees of Southern boundary of 1
%                               degree latitude band  (North positive,  South
%                               negative).
%    0      7 - 8       I2      Record  sequence  number within group (always
%                               set to ' 0' on first record).
%    0      9 - 11      I3      Number  of pairs of longitude band/correction
%                               area data values to be found on the following
%                               records of the group (9 pairs per record).
% 
%  1 - n    1 - 2       I2      File identifier (always set to ' 1').
%  1 - n    4 - 6       I4      Latitude in degrees (as on record 0).
%  1 - n    7 - 8       I2      Record sequence number within group.
%  1 - n    9 - 80   9(I5,I3)   Longitude (I5) / correction  area number (I3)
%                               data pairs, 9 pairs per record. The longitude
%                               expressed  in  degrees  from  -180  (i.e. 180
%                               degrees West) to +179 (i.e. 179 degrees East)
%                               represents   the   Western  boundary  of  the
%                               correction area within the 1 degree  latitude
%                               band.
% 
% 
% The record groups are arranged in descending order of latitude.
% 
% To  determine  the  correction area number for an  observed  latitude   and
% longitude:-
% 
%   Round the latitude value down to the nearest integer value of degrees  of
%   latitude, and identify the group of records which apply  to that latitude
%   band.  Within each group of records,  the longitude/correction area  data
%   pairs  are  arranged in ascending order of  longitude,  starting  at  180
%   degrees  West  (-180  degrees) and  progressing  Eastwards  in  irregular
%   multiples  of  whole degrees throughout  the group.  The  last  longitude
%   value given in a group indicates a longitude band which extends Eastwards
%   from that value to 180  degrees East.  Round the observed longitude value
%   down to the nearest integer value of degrees,  and identify the longitude
%   band in  which the observed value is greater than or equal to the Western
%   boundary, but less than the Eastern boundary. The correction area  number
%   associated  with the Western boundary of that longitude band is  the  one
%   which applies to the observed latitude and longitude values.
% 
% 
% 
% CORRECTN.DAT :  Echo-sounding correction tables (1500 metres/second)
% 
% 
% File contains 584 logical records.
% 
% For each of the 85  correction areas,  the corrected sounding values at 100
% metre intervals (starting at 200 metres) are contained in a group of two or
% more records. The first record identifies the correction area and indicates
% the  number  of  corrected sounding values to be  found  on  the  following
% record(s).  The  second  and subsequent  records  hold  corrected  sounding
% values.
% 
% 
% Data record formats (for each  group of records):-
% 
% Record   Columns    Format              Contents
% 
%    0      1 - 2       I2      File identifier (always set to ' 2').
%    0      3 - 5       I3      Correction area number.
%    0      6 - 7       I2      Record sequence number within group.
%    0      8 - 11      I4      Number of corrected sounding data  values  to
%                               be  found  on  the  following  records of the
%                               group (12 values per record).
% 
%  1 - n    1 - 2       I2      File identifier (always set to ' 2').
%  1 - n    3 - 5       I3      Correction area number (as on record 0).
%  1 - n    6 - 7       I2      Record sequence number within group (' 1' for
%                               first record, ' 2' for second etc.).
%  1 - n    8 - 79     12I6     Corrected  sounding  values  in metres at 100
%                               metre intervals starting at  200  metres,  12
%                               values per record.
% 
% 
% The  record  groups  are arranged in ascending  order  of  correction  area
% number.
% 
% To determine a corrected sounding value:-
% 
%   Identify  the group of records which apply to the correction area  number
%   for  that  sounding  (as determined from  BOUNDARY.DAT).  If   necessary,
%   correct  the  observed sounding to that which would  have  been  obtained
%   using  equipment  set for a sound velocity of 1500   metres  per  second.
%   Locate  the  pair  of 100 metre corrected  soundings  which  bracket  the
%   observed  value (i.e.  for an observed sounding  of 437 metres,  use  the
%   third and fourth corrected sounding values, which correspond to soundings
%   of 400 and 500 metres  respectively).  Compute the corrected sounding for
%   the precise observed sounding value by linear interpolation between those
%   values.  E.g. for an observed sounding of 437 metres:
% 
%       corrected sounding = s400 + ( ( s500 - s400 ) * 37 ) / 100
% 
%   where s400 and s500 are  corrected sounding values for 400 and 500 metres
%   respectively.
% 
% 
% BOUNDARY.IDX :  Indexed echo-sounding correction area boundary  definitions
%                 for use in a direct access file
% 
% 
% File contains 6128 logical records.  
% 
% The  area boundary information given in BOUNDARY.DAT has  been  re-arranged
% into an expanded form suitable for loading into a direct access file.  Each
% record  of the file bears an integer  number and the record number  in  the
% direct access file on which that number should be stored. The direct access
% file  should  be   capable  of storing one integer number  of  up  to  6200
% (approximately 1.5 * 2**12) on each record; 6128 records in total.  Records
% 1  to  181 of the file are the index section,  records 182 to 6127 are  the
% data section containing area boundary information.  The last  record in the
% file  (number  6128) is an end of file marker record used by  the  software
% described in FINAR.FTN, and bears the value  '-999'.
% 
% Records  in  the index section act as pointers,  each  bearing  the  record
% sequence number of the first record of a group of  records containing  area
% boundary information for a single one degree latitude band. Record number 1
% points to data for 89 degrees  (89 degrees North),  record number 2 to data
% for  88 degrees and so on up to record number 180 which points to data  for
% -90 degrees  (90 degrees South);  record number 181 points to record number
% 6128.  Within each group of records in the data section of the  file,   the
% first  (pointed  to by the index) bears the value of the latitude  band  in
% degrees;  subsequent records of the group are in pairs,   the first of each
% pair  bearing the Western boundary value in degrees and the second  bearing
% the  correction  area number (in the  sequence described  for  BOUNDARY.DAT
% above).
% 
% 
% Data record formats (for all records):-
% 
% Columns    Format              Contents
%  
%  1 - 2       I2       File identifier (always set to ' 3').
%  3 - 8       I6       Record number within  file  (starting at '     1' for
%                       first record in file). This also indicates the record
%                       number  in  the  direct access file on which the data
%                       value should be stored.
%  9 - 14      I6       Data value.  This  may be an index pointer,  latitude
%                       value,  longitude value  or  correction  area  number
%                       (see description of file format for details).
% 
% 
% A  subroutine which will scan these data (when they are stored in a  direct
% access file) and return the correction area number  relevant to a  supplied
% latitude and longitude is listed in FINAR.FTN.     
% 
% 
% CORRECTN.IDX :  Indexed     echo-sounding    correction    tables     (1500
%                 metres/second) for use in a direct access file
% 
% 
% File contains 5813  logical records.
% 
% The   corrected  sounding  information  given  in  CORRECTN.DAT  has   been
% re-arranged  into  an  expanded form suitable for loading   into  a  direct
% access file. Each record of the file bears an integer number and the record
% number  of the direct access file on which  that number should  be  stored.
% The  direct access file should be capable of storing one integer number  of
% up  to 13000 (approximately  1.6 * 2**13) on each record;  5813 records  in
% total.  Records 1 to 86 of the file are the index section,  records  87  to
% 5812  are the  data section containing corrected sounding information.  The
% last record in the file (number 5813) is an end of file marker record  used
% by the software described in DCORR.FTN, and bears the value '-999'.
% 
% Records  in  the index section act as pointers,  each bearing   the  record
% sequence  number  of  the first record of a  group  of  records  containing
% corrected sounding information for a single  correction area. Record number
% 1 points to data for correction area number 1,  record number 2 to data for
% correction area number 2  and so on up to record number 85 which points  to
% data  for  correction  area number 85; record number 86  points  to  record
